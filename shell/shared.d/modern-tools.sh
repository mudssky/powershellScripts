#!/bin/bash
# ========================================================================
# 文件: modern-tools.sh
# 作用: 启用并初始化一批 Homebrew 安装的现代 CLI 工具。
#       所有替换均为「条件式」——仅当现代工具存在时才生效，
#       未安装时回退到系统原命令，不破坏脚本与环境。
#
# 设计原则（对应需求"没安装时回退"）:
#   - 工具替换一律用 `command -v X >/dev/null 2>&1` 守护，无 else 即回退。
#   - 语法不兼容的工具（find→fd 等）不做 alias，只额外暴露新入口，
#     避免覆盖破坏依赖原语法的既有命令/脚本。
#   - 不重复其他文件已做的事：zoxide 初始化见 zz-prompt.sh，
#     eza 的 ll/tree 见 aliases.sh，本文件只补尚未被覆盖的工具。
#
# 兼容性: bash 与 zsh 双 shell，shared.d 同被两种 rc source。
# ========================================================================

# ----------------------------------------------------------------------
# 1. bat → cat（条件 alias）
#    bat 官方即为「可 alias 成 cat」设计：管道/非 tty 下自动退化为纯文本，
#    不带装饰；交互式下才语法高亮。这是唯一安全的"替换型" alias。
# ----------------------------------------------------------------------
if command -v bat >/dev/null 2>&1; then
  alias cat='bat'
  # bat 在管道里默认仍带装饰（除非自动检测为非 tty），
  # 显式提供一个「纯文本」入口，便于脚本场景：catp file | grep ...
  alias catp='bat -pp'
fi

# ----------------------------------------------------------------------
# 2. lazygit 快捷入口
#    lazygit 是交互式 TUI，无法 alias 覆盖任何现有命令（无冲突），
#    单独提供 `lg` 短命令入口即可。
# ----------------------------------------------------------------------
if command -v lazygit >/dev/null 2>&1; then
  alias lg='lazygit'
fi

# ----------------------------------------------------------------------
# 3. zoxide（智能 cd）
#    zoxide 的初始化（eval zoxide init + z/zi 注入 + zq/za/zr alias）
#    已在 zz-prompt.sh 完成，此处不重复，避免重复 eval 导致数据库锁竞争
#    与函数重复定义。本文件仅做说明，便于维护者查阅。
#
#    用法（由 zz-prompt.sh 注入）:
#      z <关键词>     智能跳转（按访问频率）
#      zi <关键词>    交互式选择（内嵌 fzf）
#      zq/za/zr       query/add/remove
# ----------------------------------------------------------------------

